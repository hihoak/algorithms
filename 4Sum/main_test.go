package _Sum

import (
	"github.com/stretchr/testify/require"
	"testing"
)

func TestFunc(t *testing.T) {
	cases := []struct {
		nums   []int
		target int
		res    [][]int
	}{
		//{
		//	[]int{1, 0, -1, 0, -2, 2},
		//	0,
		//	[][]int{
		//		{-2, -1, 1, 2}, {-2, 0, 0, 2}, {-1, 0, 0, 1},
		//	},
		//},
		//{
		//	[]int{-1, -1, -1, -1, -1, -1, -1},
		//	-4,
		//	[][]int{
		//		{-1, -1, -1, -1},
		//	},
		//},
		//{
		//	[]int{},
		//	-4,
		//	[][]int{},
		//},
		//{
		//	[]int{1, 2, 3, 4, 5, 6},
		//	-4,
		//	[][]int{},
		//},
		//{
		//	[]int{-1, -2, 3, 4, 5, 6},
		//	1,
		//	[][]int{},
		//},
		//{
		//	[]int{-1, -2, 3, 4, 5, 6},
		//	4,
		//	[][]int{
		//		{-2, -1, 3, 4},
		//	},
		//},
		//{
		//	[]int{-1, -2, 0, 3, 4, 5, 6, 7},
		//	4,
		//	[][]int{
		//		{-2, -1, 0, 7},
		//		{-2, -1, 3, 4},
		//	},
		//},
		//{
		//	[]int{-3, -1, -2, 0, 3, 4, 5, 6, 7},
		//	4,
		//	[][]int{
		//		{-3, -2, 3, 6},
		//		{-3, -2, 4, 5},
		//		{-3, -1, 3, 5},
		//		{-3, 0, 3, 4},
		//		{-2, -1, 0, 7},
		//		{-2, -1, 3, 4},
		//	},
		//},
		{
			[]int{-2, -1, -1, 1, 1, 2, 2},
			0,
			[][]int{
				{-2, -1, 1, 2},
				{-1, -1, 1, 1},
			},
		},
	}

	for _, tc := range cases {
		require.Equal(t, tc.res, fourSum(tc.nums, tc.target))
	}
}
